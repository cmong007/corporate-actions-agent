@tool
def draft_internal_notification(
    event_type: str | None,
    event_category: str | None,
    isin: str | None,
    issuer: str | None,
    record_date: str | None,
    ex_date: str | None,
    pay_date: str | None,
    election_deadline: str | None,
    gross_rate: str | None,
    currency: str | None,
    entitlements: List[dict],
    total_projected: str | None,
    urgency: str | None,
    narrative: str | None = ""
) -> str:
    """
    Draft a professional internal Corporate Actions notification.

    Formats a structured alert suitable for distribution to Portfolio
    Managers, Traders, and Operations teams. Includes all key event
    details, per-portfolio entitlements, and required actions.

    Returns:
        Formatted notification string.
    """
    event_type = event_type or ""
    event_category = event_category or "mandatory"
    isin = isin or ""
    issuer = issuer or "Unknown Issuer"
    record_date = record_date or ""
    ex_date = ex_date or ""
    pay_date = pay_date or ""
    election_deadline = election_deadline or ""
    gross_rate = gross_rate or "0"
    currency = currency or "USD"
    total_projected = total_projected or "0"
    urgency = urgency or "normal"
    narrative = narrative or ""

    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    urgency_banner = {
        "critical": "🔴 CRITICAL — IMMEDIATE ACTION REQUIRED",
        "high":     "🟡 HIGH PRIORITY",
        "normal":   "🟢 FOR INFORMATION"
    }.get(urgency, "🟢 FOR INFORMATION")

    category_label = {
        "mandatory": "MANDATORY (No action required from PM)",
        "voluntary": "⚡ VOLUNTARY — PM ELECTION REQUIRED",
        "elective":  "⚡ ELECTIVE — PM ELECTION REQUIRED"
    }.get(event_category, event_category.upper())

    event_labels = {
        "DVCA": "Cash Dividend",
        "SPLF": "Stock Split",
        "MRGR": "Merger / Acquisition",
        "TEND": "Tender Offer",
        "RHTS": "Rights Issue",
        "BONU": "Bonus Issue"
    }
    event_label = event_labels.get(event_type, event_type)

    lines = [
        f"CORPORATE ACTIONS NOTIFICATION",
        f"Issued: {timestamp}",
        f"{'='*60}",
        f"{urgency_banner}",
        f"{'='*60}",
        f"",
        f"EVENT DETAILS",
        f"-" * 40,
        f"Event Type    : {event_label} ({event_type})",
        f"Category      : {category_label}",
        f"Issuer        : {issuer}",
        f"ISIN          : {isin}",
        f"Record Date   : {record_date}",
        f"Ex-Date       : {ex_date}",
        f"Pay/Settle    : {pay_date}",
    ]

    if election_deadline:
        lines.append(f"Election Deadline : ⚠️  {election_deadline} (UTC)")

    if gross_rate and gross_rate != "0":
        lines += [
            f"Rate          : {currency} {gross_rate} per share (gross)",
        ]

    lines += ["", "AFFECTED PORTFOLIOS & PROJECTED ENTITLEMENTS", "-" * 40]

    for ent in entitlements:
        if ent.get("type") == "shares":
            lines.append(
                f"  {ent['portfolio_name']:<25} {ent['quantity']:>10,} shares "
                f"→ {ent.get('new_shares', 'TBC'):>10,} new shares"
            )
        else:
            lines.append(
                f"  {ent['portfolio_name']:<25} {ent['quantity']:>10,} shares "
                f"→ {currency} {float(ent.get('gross_entitlement', 0)):>12,.2f} gross"
            )

    lines += [
        f"",
        f"  TOTAL PROJECTED: {currency} {float(total_projected):,.2f}",
        f"",
    ]

    if event_category in ("voluntary", "elective"):
        lines += [
            "REQUIRED ACTION",
            "-" * 40,
            "Portfolio Manager must confirm election decision before deadline.",
            "Please respond with: TENDER / DO NOT TENDER / PARTIAL (specify %)",
            "Contact Ops team immediately if unable to respond before deadline.",
            "",
        ]
    else:
        lines += [
            "ACTION",
            "-" * 40,
            "No election required. Entitlements will be processed automatically.",
            "Ops team will confirm upon receipt of custodian MT566 confirmation.",
            "",
        ]

    if narrative:
        lines += ["CUSTODIAN NARRATIVE", "-" * 40, narrative[:500], ""]

    lines += [
        "=" * 60,
        "This notification was generated by the CA Processing Agent.",
        "For queries, contact the Corporate Actions Operations team.",
    ]

    return "\n".join(lines)


@tool
def tag_recipients(event_category: str, urgency: str, asset_class: str = "Equity") -> list:
    """
    Determine the correct internal recipients for a CA notification.

    Args:
        event_category: "mandatory" | "voluntary" | "elective"
        urgency: "critical" | "high" | "normal"
        asset_class: "Equity" | "Fixed Income"

    Returns:
        List of recipient role strings.
    """
    recipients = ["operations_team", "ca_analyst"]

    if event_category in ("voluntary", "elective"):
        recipients.append("portfolio_manager")
        recipients.append("trader")

    if urgency == "critical":
        recipients.append("senior_management")
        recipients.append("compliance")

    if urgency in ("critical", "high"):
        recipients.append("risk_team")

    if asset_class == "Fixed Income":
        recipients.append("fixed_income_analyst")

    return list(set(recipients))  # deduplicate


