@tool
def validate_security_identifiers(
    isin: str,
    cusip: str = "",
    sedol: str = ""
) -> dict:
    """
    Validate the format of ISIN, CUSIP, and SEDOL identifiers.

    Checks format rules only (not database lookup).
    ISIN: 12 chars, 2 alpha + 10 alphanumeric, Luhn checksum
    CUSIP: 9 chars alphanumeric
    SEDOL: 7 chars, first 6 alphanumeric + check digit

    Returns:
        Dict with validation results per identifier.
    """
    from ca_agent.tools.ingestion_tools import _isin_checksum_valid

    results = {}

    # ISIN
    isin_clean = isin.strip().upper()
    results["isin"] = {
        "value": isin_clean,
        "format_valid": bool(re.match(r'^[A-Z]{2}[A-Z0-9]{10}$', isin_clean)),
        "checksum_valid": _isin_checksum_valid(isin_clean),
        "issues": []
    }
    if not results["isin"]["format_valid"]:
        results["isin"]["issues"].append("ISIN must be 12 chars: 2 letters + 10 alphanumeric")
    if results["isin"]["format_valid"] and not results["isin"]["checksum_valid"]:
        results["isin"]["issues"].append("ISIN checksum digit invalid (ISO 6166)")

    # CUSIP
    if cusip:
        cusip_clean = cusip.strip().upper()
        results["cusip"] = {
            "value": cusip_clean,
            "format_valid": bool(re.match(r'^[A-Z0-9]{9}$', cusip_clean)),
            "issues": []
        }
        if not results["cusip"]["format_valid"]:
            results["cusip"]["issues"].append("CUSIP must be exactly 9 alphanumeric characters")
    else:
        results["cusip"] = {"value": "", "format_valid": None, "issues": ["Not provided"]}

    # SEDOL
    if sedol:
        sedol_clean = sedol.strip().upper()
        results["sedol"] = {
            "value": sedol_clean,
            "format_valid": bool(re.match(r'^[A-Z0-9]{6}\d$', sedol_clean)),
            "issues": []
        }
        if not results["sedol"]["format_valid"]:
            results["sedol"]["issues"].append("SEDOL must be 6 alphanumeric chars + 1 check digit")
    else:
        results["sedol"] = {"value": "", "format_valid": None, "issues": ["Not provided"]}

    all_valid = all(
        v.get("format_valid") is not False
        for v in results.values()
    )
    results["all_valid"] = all_valid

    return results


@tool
def check_security_master_record(isin: str) -> dict:
    """
    Look up a security in the Security Master file and identify
    any data quality issues.

    Checks for: missing mandatory fields, incorrect asset class,
    inactive securities, missing exchange codes.

    Args:
        isin: ISIN to look up.

    Returns:
        Dict with security record, issues list, and suggested fixes.
    """
    sm_file = SECURITY_MASTER_FILE
    if not os.path.exists(sm_file):
        return {
            "found": False,
            "issues": [f"Security master file not found: {sm_file}"],
            "suggested_fixes": ["Ensure SECURITY_MASTER_FILE env var is set correctly"]
        }

    with open(sm_file, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row.get("isin", "").strip().upper() == isin.upper():
                issues = []
                fixes = []

                mandatory_fields = {
                    "cusip": "CUSIP",
                    "sedol": "SEDOL",
                    "security_name": "Security Name",
                    "asset_class": "Asset Class",
                    "exchange": "Exchange",
                    "currency": "Currency",
                    "issuer_country": "Issuer Country"
                }

                for field, label in mandatory_fields.items():
                    if not row.get(field, "").strip():
                        issues.append(f"Missing mandatory field: {label}")
                        fixes.append(
                            f"Populate '{label}' field for {isin} in Security Master. "
                            f"Source from Bloomberg/Refinitiv or custodian confirmation."
                        )

                # Check active status
                if row.get("is_active", "").strip().lower() == "false":
                    issues.append("Security marked as INACTIVE in Security Master")
                    fixes.append(
                        "Verify if security should be reactivated. "
                        "Check if corporate action has changed the security status."
                    )

                return {
                    "found": True,
                    "record": dict(row),
                    "issues": issues,
                    "suggested_fixes": fixes,
                    "data_quality_score": f"{max(0, 100 - len(issues) * 15)}%"
                }

    return {
        "found": False,
        "record": {},
        "issues": [f"ISIN {isin} not found in Security Master"],
        "suggested_fixes": [
            f"Add new security record for {isin} to Security Master. "
            "Obtain all mandatory fields from custodian or data vendor before adding."
        ]
    }
